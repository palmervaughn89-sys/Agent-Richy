// ── Helper: deduplicate when same ticker maps to multiple brands ─────
export function deduplicateBrands(
  selected: { brand: Brand; monthlySpend: number }[],
): { brand: Brand; monthlySpend: number }[] {
  const map = new Map<string, { brand: Brand; monthlySpend: number }>();
  for (const entry of selected) {
    const existing = map.get(entry.brand.ticker);
    if (existing) {
      existing.monthlySpend += entry.monthlySpend;
    } else {
      map.set(entry.brand.ticker, { ...entry });
    }
  }
  return Array.from(map.values());
}

